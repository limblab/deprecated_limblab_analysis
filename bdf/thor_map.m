

function output=thor_map(spike_list)

%mapping from electrode number to electrode position
array_map=[93 92 94 95 75 96 85 97 86 98;...
           87 88 77 99 66 89 76 90 67 79;...
           58 80 78 100 68 60 69 50 59 40;...
           49 30 83 84 73 74 63 64 53 54;...
           43 55 44 45 33 46 34 65 24 56;...
           35 47 25 57 26 36 27 37 28 38;...
           29 48 19 39 81 82 71 72 61 62;...
           51 52 41 42 31 32 21 22 11 12;...
           1  23 3  13 4  14 15 5  16 6;...
           17 91 8  18 20 10 0  0  0  0 ];
 
%because indexing increases as you go down a column instead of across a row...       
array_map_T=transpose(array_map);       
       
num_units =size(spike_list,1); %number of units

%for each unit
for i=1:num_units
    pin       = spike_list(i);
    index     = array_map_T(pin);
    cell      =ind2sub([10 10],index);
    subs(i,:) =cell;
end

output=subs;